expenses = [300, 200, 400, 600, 150]
total_expenses = 0

for x in expenses:
    total_expenses = total_expenses + x

print("The total expenses are: ksh", total_expenses, sep="")   #separater is used to avoid space between ksh and sum value

prices = [1000, 2000, 3000, 4000, 5000]
total_prices = sum(prices)
print("The total prices are: ksh", total_prices, sep="")